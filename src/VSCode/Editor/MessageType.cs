namespace VSCode.Editor
{
    /// <summary>
    /// Represents a type of log or UI message.
    /// </summary>
    public enum MessageType
    {
        /// <summary>
        /// Error-Level Message
        /// </summary>
        Error = 1,

        /// <summary>
        /// Warning-Level Message
        /// </summary>
        Warning = 2,

        /// <summary>
        /// Info-Level Message
        /// </summary>
        Info = 3,

        /// <summary>
        /// Log-Level Message
        /// </summary>
        Log = 4
    }
}
